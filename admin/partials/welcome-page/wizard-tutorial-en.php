An ingenious and not to be despised side effect is that your reviews automatically appear with stars in Google's organic search results. For you, this means that Google adds stars to your page and that attracts more visitors from Google.

<img class="size-medium wp-image-46937 aligncenter" src="https://saleswonder.biz/wp-content/uploads/sterne-bei-den-google-suchergebnissen-1030x216.jpg" alt="" width="100%" />

However, that's not all! Through the links in the rating request emails, customers will rate on your own review page. This minimalistic site is automatically created by the plugin and contains all recently purchased products. This has the advantage that your customers can easily and quickly submit reviews and very likely rate several products at once.

<img class="alignnone size-medium wp-image-46937" src="https://saleswonder.biz/wp-content/uploads/Brandneues_WooCommerce-Bewertungs-Feature-800px.gif" alt="" width="100%" />

Further noteworthy is the possibility to forward customers based on the given rating stars. This  is done by linking the stars and buttons in the e-mail. Example: 1-3 star ratings will be redirected to your support contact form, and those with 4-5 stars will be linked to the rating page. **That leads to more better reviews** and less unsatisfied customers.

