Genialer und nicht zu verachtender Nebeneffekt ist, dass die Bewertungen automatisch mit Sternen in den organischen Suchergebnissen in Google angezeigt werden können. Das bedeutet für dich, dass Google deine Seite mit Sternen auszeichnet und du dadurch mehr Besucher von Google bekommst.

<img class="alignnone size-medium wp-image-46937" src="https://saleswonder.biz/wp-content/uploads/sterne-bei-den-google-suchergebnissen-1030x216.jpg" alt="" width="100%" />

Das war aber noch nicht alles! Über den Links in der Bewertungsanfrage-E-Mails können deine Kunden auf deiner eigenen Bewertungsseite bewerten. Diese minimalistische Seite wird automatisch vom Plugin erstellt und enthält alle zuletzt gekauften Produkte des Kunden. Das hat den Vorteil, dass deine Kunden einfach und schnell Bewertungen abgeben können und dadurch sehr wahrscheinlich mehrere Produkte in Folge bewerten.

<img class="alignnone size-medium wp-image-46937" src="https://saleswonder.biz/wp-content/uploads/Brandneues_WooCommerce-Bewertungs-Feature-800px.gif" alt="" width="100%" />

Besonders hervorzuheben ist die Möglichkeit der gezielten Weiterleitung des Kunden nach abgegebenen Sternen, d.h. die Sterne und Buttons in der E-Mail können beliebig verlinkt werden. Beispiel: Bewertungen mit 1-3 Sternen leiten auf dein Support-Kontaktformular weiter und die mit 4-5 Sternen werden mit der Bewertungsseite verlinkt. Das stellt sicher, dass nur gute Bewertungen veröffentlicht werden und unzufriedenen Kunden geholfen wird.

Optional aber häufig genutzt:
<ul>
 	<li><strong>Für eine höhere Click-Through-Rate</strong>, zeige die Bewertungen auch auf deiner Landingpage an. Nutze dazu unser <a href="https://downloads.wordpress.org/plugin/woo-product-reviews-shortcode.zip" target="_blank" rel="noopener"> freies Shortcode Plugin</a>. Auch der Code für die Sterne für die Google Suchergebnissen wird auf der Seite eingefügt. Das bewirkt das Google Sterne erkennt und anzeigen kann.</li>
 	<li><strong>Mehr Bewertungen durch höhere e-Mail-Zustellrate</strong>, indem du deine Bewertungsanfragen zu Klick-Tipp übertragen lässt und Klick-Tipp die E-Mails automatisch über die vorgefertigte Kampagne versendet. Wähle dazu Klick-Tipp zum Versenden der E-Mails <a href="?page=wp2leads-wtsr&tab=email" target="_blank">hier aus.</a></li>
 	<li><strong>Für eine größere Glaubwürdigkeit deiner Bewertungen</strong>, verlinke die Bewertungsanfrage-E-Mails mit Trusted-Shops und veröffentliche die Bewertungen im dortigen Portal. Gebe bitte die Trusted Shops-Anmeldeinformationen <a href="?page=wp2leads-wtsr&tab=settings" target="_blank">hier ein</a> um den Modus zu aktivieren.</li>
 	<li><strong>Für ein höheres Produktvertrauen</strong> zeige die Trusted-Shops-Bewertungen in deinem Shop über das Theme und Plugin von Vndidero (Dennis) an.</li>
</ul>